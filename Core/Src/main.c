void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
    	if (htim== &htim2)
    	  {

    	    ms_count += 10; // Increment millisecond count (10 ms tick)
    	    if(ms_count%100==0 )
    	    {
    	    	adc_values[Sample_Index][0]=adc_read[0]+adc_offsets[0];
    	    	adc_values[Sample_Index][1]=adc_read[1]+adc_offsets[1];
    	    	adc_values[Sample_Index][2]=adc_read[2]+adc_offsets[2];
    	    	Sample_Index++;
					
    	    	if(Sample_Index>=10)
    	    	{
    	    		Samples_Collected=1;
    	    		Sample_Index=0;
							

    	    	}
						
    	    }
		if (Samples_Collected == 1) {
			
            // Calculate current values
            for (int j = 0; j < ADC_CHANNELS; j++) {
							for (int i = 0; i < SAMPLE_COUNT; i++) {
								 float voltage = (((float)adc_values[i][j]) * 3.3 )/ 4095;

						// Convert voltage to current (in amperes)
						 currents[i][j] = (voltage-2.5) / SENSITIVITY;
                
								 
            }
					}

            // Calculate averages
            for (int j = 0; j < ADC_CHANNELS; j++) {
                float sum = 0;
                for (int i = 0; i < SAMPLE_COUNT; i++) {
                    sum += currents[i][j];
                }
                adc_averages[j] = sum / SAMPLE_COUNT;
            }
						calculation=(adc_averages[0]+adc_averages[1]+adc_averages[2])/3;
					 final_value=calculation*0.2+calculation;

            Samples_Collected = 0;
           
        }
		  if (ms_count >= 1000)// Display on UART after 1 second
				{
				char uart_str[50];
        sprintf(uart_str, "%f, \r\n",final_value);
        HAL_UART_Transmit(&huart2, (uint8_t *)uart_str, strlen(uart_str), HAL_MAX_DELAY);
				
					 
			}

    	    if (ms_count >= 2000) // Display on SSD1306 after 2 seconds
    	    {
    	    	char display_str[50];
    	    		 
    	    				
    	    	
    	    				ssd1306_SetCursor(0, 22); // ssd1306_SetCursor(0, 14);
    	    				sprintf(display_str,"AVG=%.2f A    ",final_value);
    	    				ssd1306_WriteString(display_str,Font_11x18, White);
    	    				
    	    				  ssd1306_UpdateScreen(&hi2c1);
    	      ms_count = 0; // Reset millisecond count
    	    }

    	    
    }
    
}



